char *decode_ustring (int length, uchar *buf)
{
	static char str[1024]; // чтобы не освобождать все-время память

	int i;
	for (i = 0; i < length; i++)
	{
		ushort c = buf[2*i] | (buf[2*i+1] << 8);
		int j;
		for (j = 0; j < 0xFF; j++)
			if ( c == codepage_ru866[j] )
			{
				str[i] = j;
				break;
			}			
		str[i] = j < 0xFF ? j : '?';
	}
	str[i] = 0;
	
	return str;
}

